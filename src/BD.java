    private void registrarUsuario() {
        String nombreUsuario = txtNuevoUsuario.getText();
        String contrasena = String.valueOf(txtNuevaContrasena.getPassword());
        String confirmarContrasena = String.valueOf(txtconfirmar.getPassword());

        if (nombreUsuario.isEmpty() || contrasena.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.");
        }else if(!contrasena.equals(confirmarContrasena)){
          JOptionPane.showMessageDialog(null, "la contrase単a no coinciden"); 
        }else {
            usuarioManager.agregarUsuario(new Usuario(nombreUsuario, contrasena));
            JOptionPane.showMessageDialog(null, "Usuario registrado con exito.");
            this.dispose();
        }
    }
