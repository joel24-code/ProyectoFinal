    void leer() {
        File archivo = new File(c, "usuario.txt");
        if (archivo.exists()) {
            try (Scanner scanner = new Scanner(archivo)) {
                while (scanner.hasNextLine()) {
                    System.out.println(scanner.nextLine());
                }
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Error al leer el archivo");
            }
        } else {
            try (FileWriter fw = new FileWriter(archivo)) {
                fw.write("");
                JOptionPane.showMessageDialog(this, "Archivo creado");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Error al crear el archivo");
            }
        }
    }
    
   private void Modificar() {
        String archivo = txtnombre.getText() + ".registros";
        File modifica_archivo = new File(ubicacion + archivo);
        
        if (txtnombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "No hay nombre");
        } else {
            try {
                if (!modifica_archivo.exists()) {
                    JOptionPane.showMessageDialog(rootPane, "El archivo no existe");
                } else {
                    try (FileWriter modifica = new FileWriter(modifica_archivo)) {
                    modifica.write(
                    "nombre=" + txtnombre.getText() + "\r\n" +
                    "apellido=" + txtapellido.getText() + "\r\n" +
                    "correo=" + txtcorreo.getText() + "\r\n" +
                    "usuario=" + txtusuario.getText() + "\r\n" +
                    "contrase単a=" + txtcontrasena.getText() + "\r\n" +
                    "confirmar=" + txtconfirmar.getText() + "\r\n"
                        );
                    }
                    JOptionPane.showMessageDialog(rootPane, "Archivo modificado");
                }
            } catch (IOException e) {
                JOptionPane.showMessageDialog(rootPane, "Archivo no se ha modificado");
            }
        }
    }
    
    private void Eliminar() {
        String archivo = txtnombre.getText() + ".registros";
        File elimina_archivo = new File(ubicacion + archivo);
        
        if (txtnombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "No hay nombre");
        } else {
            if (!elimina_archivo.exists()) {
                JOptionPane.showMessageDialog(rootPane, "El archivo no existe");
            } else {
                elimina_archivo.delete();
                JOptionPane.showMessageDialog(rootPane, "Archivo eliminado");
            }
        }
    }

       

